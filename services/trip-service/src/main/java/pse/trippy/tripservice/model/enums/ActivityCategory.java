package pse.trippy.tripservice.model.enums;

/**
 * Category that classifies an activity within a day plan.
 */
public enum ActivityCategory {

    /** Hotel, hostel, or other lodging. */
    ACCOMMODATION,

    /** Flights, trains, buses, car hire, or other transport. */
    TRANSPORT,

    /** Restaurants, cafés, street food, or any dining experience. */
    FOOD,

    /** Museums, landmarks, tours, or cultural experiences. */
    SIGHTSEEING,

    /** Outdoor pursuits, sports, or recreational activities. */
    ACTIVITY,

    /** Retail shopping, markets, or souvenir stops. */
    SHOPPING,

    /** Any activity that does not fit another category. */
    OTHER
}
